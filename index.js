function calcAddition() {
    let x = Number(prompt('Введите первое число:'));
    let y = Number(prompt('Введите второе число:'));
    return (x + y);
}

function calcSubtraction() {
    let x = Number(prompt('Введите первое число:'));
    let y = Number(prompt('Введите второе число:'));
    return (x - y);
}

function calcMultiplication() {
    let x = Number(prompt('Введите первое число:'));
    let y = Number(prompt('Введите второе число:'));
    return (x * y);    
}

function calcDivision() {
    let x = Number(prompt('Введите первое число:'));
    let y = Number(prompt('Введите второе число:'));
    return (x / y);
}